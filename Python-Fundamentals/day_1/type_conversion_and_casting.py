#Type_conversion
x = 1    # int
y = 2.8  # float
z = 1j   # complex

#convert from int to float:
a = float(x)

#convert from float to int:
b = int(y)

#convert from int to complex:
c = complex(x)

print(a)
print(b)
print(c)

print(type(a))
print(type(b))
print(type(c))

#Type_casting

x = float(1)   # x will be 1.0
y = str(2.8) # y will be 2.8
z = int("3") # z will be 3

print(type(x))
print(type(y))
print(type(z))